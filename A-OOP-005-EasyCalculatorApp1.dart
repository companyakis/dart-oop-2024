//easycalculator.dart file

class EasyCalculator {
  late double x;
  late double y;

  double addition(x, y) {
    return x + y;
  }

  double subtraction(x, y) {
    return x - y;
  }

  double multiplication(x, y) {
    return x * y;
  }

  double division(x, y) {
    return x / y;
  }

  double square(x) {
    return x * x;
  }

  double cube(x) {
    return x * x * x;
  }

}
